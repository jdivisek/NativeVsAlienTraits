###############################################
#     SIGNIFICANCE CODES FOR P-VALUES         #
###############################################

sig.code <- function(x)
{
  if(x <= 0.001){sig <- "***"}
  if(x > 0.001 & x <= 0.01){sig <- "**"}
  if(x > 0.01 & x <= 0.05){sig <- "*"}
  if(x > 0.05){sig <- ""}
  return(sig)
}